CREATE OR REPLACE VIEW MONITORING_DB.AI_OBSERVABILITY.STG_SNOWFLAKE_INTELLIGENCE_OBSERVABILITY(
    AGENT_NAME,
    THREAD_ID,
    REQUEST_ID,
    INPUT_ID,
    SESSION_ID,
    ROLE_ID,
    ROLE_NAME,
    USER_ID,
    USER_NAME,
    OBSERVED_TIMESTAMP,
    DURATION,
    RECORD_DETAILS,
    CORTEX_ANALYST_FLAG,
    CORTEX_SEARCH_FLAG,
    CORTEX_CHART_FLAG,
    CUSTOM_TOOLS_FLAG,
    REQUEST_STATUS_DETAILS,
    STATUS,
    PLANNING_CUSTOM_ORCHESTRATION_INSTRUCTIONS,
    PLANNING_INSTRUCTIONS,
    PLANNING_MESSAGES,
    PLANNING_MODEL,
    PLANNING_QUERY,
    PLANNING_RESPONSE,
    PLANNING_TOKEN_COUNT_CACHE_READ_INPUT,
    PLANNING_TOKEN_COUNT_CACHE_WRITE_INPUT,
    PLANNING_TOKEN_COUNT_INPUT,
    PLANNING_TOKEN_COUNT_OUTPUT,
    PLANNING_TOKEN_COUNT_PLAN,
    PLANNING_TOKEN_COUNT_TOTAL,
    PLANNING_TOOL_DESCRIPTION,
    PLANNING_TOOL_NAME,
    PLANNING_TOOL_PARAMETERS,
    PLANNING_TOOL_TYPE,
    PLANNING_TOOL_EXECUTION_ARGUMENT_NAME,
    PLANNING_TOOL_EXECUTION_ARGUMENT_VALUE,
    PLANNING_TOOL_EXECUTION_ID,
    PLANNING_TOOL_EXECUTION_NAME,
    PLANNING_TOOL_EXECUTION_RESULTS,
    FEEDBACK,
    FEEDBACK_MESSAGE
) AS (
    SELECT
        RECORD_ATTRIBUTES:"snow.ai.observability.object.name"::TEXT AS AGENT_NAME,
        RECORD_ATTRIBUTES:"snow.ai.observability.agent.thread_id"::INTEGER AS THREAD_ID,
        RECORD_ATTRIBUTES:"ai.observability.record_id"::TEXT AS REQUEST_ID,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"ai.observability.input_id"::TEXT)[0]::TEXT AS INPUT_ID,
        ARRAY_AGG(DISTINCT RESOURCE_ATTRIBUTES:"snow.session.id"::TEXT) AS SESSION_ID,
        RESOURCE_ATTRIBUTES:"snow.session.role.primary.id"::TEXT AS ROLE_ID,
        RESOURCE_ATTRIBUTES:"snow.session.role.primary.name"::TEXT AS ROLE_NAME,
        RESOURCE_ATTRIBUTES:"snow.user.id"::TEXT AS USER_ID,
        RESOURCE_ATTRIBUTES:"snow.user.name"::TEXT AS USER_NAME,
        COALESCE(MIN(OBSERVED_TIMESTAMP), MIN(TIMESTAMP), MIN(START_TIMESTAMP)) AS OBSERVED_TIMESTAMP,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.duration"::INTEGER) AS DURATION,
        ARRAY_AGG(DISTINCT RECORD:"name"::TEXT) AS RECORD_DETAILS,
        ARRAY_TO_STRING(RECORD_DETAILS, ',') ILIKE '%CortexAnalyst%' AS CORTEX_ANALYST_FLAG,
        ARRAY_TO_STRING(RECORD_DETAILS, ',') ILIKE '%CortexSearchService%' AS CORTEX_SEARCH_FLAG,
        ARRAY_TO_STRING(RECORD_DETAILS, ',') ILIKE '%CortexChart%' AS CORTEX_CHART_FLAG,
        ARRAY_TO_STRING(RECORD_DETAILS, ',') ILIKE '%ToolCall%' AS CUSTOM_TOOLS_FLAG,
        OBJECT_CONSTRUCT(
            'record', ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.status"),
            'planning', ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.status"),
            'sql_execution', ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.tool.sql_execution.status"),
            'cortex_search', ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.tool.cortex_search.status"),
            'cortex_analyst', ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.tool.cortex_analyst.status"),
            'custom_tools', ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.tool.custom_tool.status")
        ) AS REQUEST_STATUS_DETAILS,
        CASE
            WHEN ARRAY_TO_STRING(REQUEST_STATUS_DETAILS:"record", ',') = 'ERROR' THEN 'Failed'
            WHEN ARRAY_TO_STRING(REQUEST_STATUS_DETAILS:"planning", ',') LIKE '%ERROR%' THEN 'Finished with Errors'
            WHEN ARRAY_TO_STRING(REQUEST_STATUS_DETAILS:"sql_execution", ',') LIKE '%ERROR%' THEN 'Finished with Errors'
            WHEN ARRAY_TO_STRING(REQUEST_STATUS_DETAILS:"cortex_analyst", ',') LIKE '%ERROR%' THEN 'Finished with Errors'
            WHEN ARRAY_TO_STRING(REQUEST_STATUS_DETAILS:"cortex_search", ',') LIKE '%ERROR%' THEN 'Finished with Errors'
            WHEN ARRAY_TO_STRING(REQUEST_STATUS_DETAILS:"custom_tools", ',') LIKE '%ERROR%' THEN 'Finished with Errors'
            ELSE 'Successfully Finished'
        END AS STATUS,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.custom_orchestration_instructions"::TEXT)[0]::TEXT AS PLANNING_CUSTOM_ORCHESTRATION_INSTRUCTIONS,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.instruction"::TEXT)[0]::TEXT AS PLANNING_INSTRUCTIONS,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.messages"::TEXT) AS PLANNING_MESSAGES,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.model"::TEXT)[0]::TEXT AS PLANNING_MODEL,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.query"::TEXT)[0]::TEXT AS PLANNING_QUERY,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.response"::TEXT)[0]::TEXT AS PLANNING_RESPONSE,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.token_count.cache_read_input"::INTEGER) AS PLANNING_TOKEN_COUNT_CACHE_READ_INPUT,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.token_count.cache_write_input"::INTEGER) AS PLANNING_TOKEN_COUNT_CACHE_WRITE_INPUT,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.token_count.input"::INTEGER) AS PLANNING_TOKEN_COUNT_INPUT,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.token_count.output"::INTEGER) AS PLANNING_TOKEN_COUNT_OUTPUT,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.token_count.plan"::INTEGER) AS PLANNING_TOKEN_COUNT_PLAN,
        SUM(RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.token_count.total"::INTEGER) AS PLANNING_TOKEN_COUNT_TOTAL,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool.description"::TEXT)[0]::TEXT AS PLANNING_TOOL_DESCRIPTION,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool.name"::TEXT) AS PLANNING_TOOL_NAME,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool.parameters"::TEXT) AS PLANNING_TOOL_PARAMETERS,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool.type"::TEXT) AS PLANNING_TOOL_TYPE,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool_execution.argument.name"::TEXT) AS PLANNING_TOOL_EXECUTION_ARGUMENT_NAME,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool_execution.argument.value"::TEXT) AS PLANNING_TOOL_EXECUTION_ARGUMENT_VALUE,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool_execution.id"::TEXT) AS PLANNING_TOOL_EXECUTION_ID,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool_execution.name"::TEXT) AS PLANNING_TOOL_EXECUTION_NAME,
        ARRAY_AGG(DISTINCT RECORD_ATTRIBUTES:"snow.ai.observability.agent.planning.tool_execution.results"::TEXT) AS PLANNING_TOOL_EXECUTION_RESULTS,
        CASE
            ARRAY_AGG(VALUE:"positive") WITHIN GROUP (ORDER BY OBSERVED_TIMESTAMP DESC)[0]::TEXT
            WHEN 'true' THEN 'Positive'
            WHEN 'false' THEN 'Negative'
        END AS FEEDBACK,
        ARRAY_AGG(VALUE:"feedback_message") WITHIN GROUP (ORDER BY OBSERVED_TIMESTAMP DESC)[0]::TEXT AS FEEDBACK_MESSAGE
    FROM SNOWFLAKE.LOCAL.AI_OBSERVABILITY_EVENTS tr
    WHERE 1 = 1
      AND SCOPE:"name"::TEXT = 'snow.cortex.agent'
    GROUP BY ALL
);

