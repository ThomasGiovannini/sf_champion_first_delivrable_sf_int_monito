CREATE OR REPLACE VIEW MONITORING_DB.AI_OBSERVABILITY.FACT_SNOWFLAKE_INTELLIGENCE_REQUESTS_DETAILS(
    REQUEST_ID,
    USAGE_DATE,
    USER_NAME,
    CHILD_REQUEST_ID,
    REQUEST_TYPE,
    LLM,
    START_TIME,
    TOKENS_READ_INPUT,
    TOKENS_WRITE_INPUT,
    TOKENS_INPUT,
    TOKENS_OUTPUT,
    CREDITS_READ_INPUT,
    CREDITS_WRITE_INPUT,
    CREDITS_INPUT,
    CREDITS_OUTPUT
) AS (
    SELECT
        si.REQUEST_ID,
        si.USAGE_DATE,
        si.USER_NAME,
        OBJECT_KEYS(t1.VALUE)[0]::TEXT AS CHILD_REQUEST_ID,
        t2.KEY AS REQUEST_TYPE,
        t3.KEY AS LLM,
        t1.VALUE[OBJECT_KEYS(t1.VALUE)[0]]:"start_time"::TIMESTAMP_TZ AS START_TIME,
        COALESCE(t3.VALUE:"cache_read_input", 0)::INTEGER AS TOKENS_READ_INPUT,
        COALESCE(t3.VALUE:"cache_write_input", 0)::INTEGER AS TOKENS_WRITE_INPUT,
        COALESCE(t3.VALUE:"input", 0)::INTEGER AS TOKENS_INPUT,
        COALESCE(t3.VALUE:"output", 0)::INTEGER AS TOKENS_OUTPUT,
        COALESCE(TRY_PARSE_JSON(CREDITS_GRANULAR[t1.INDEX])[OBJECT_KEYS(t1.VALUE)[0]][t2.KEY][t3.KEY]:"cache_read_input", 0)::FLOAT AS CREDITS_READ_INPUT,
        COALESCE(TRY_PARSE_JSON(CREDITS_GRANULAR[t1.INDEX])[OBJECT_KEYS(t1.VALUE)[0]][t2.KEY][t3.KEY]:"cache_write_input", 0)::FLOAT AS CREDITS_WRITE_INPUT,
        COALESCE(TRY_PARSE_JSON(CREDITS_GRANULAR[t1.INDEX])[OBJECT_KEYS(t1.VALUE)[0]][t2.KEY][t3.KEY]:"input", 0)::FLOAT AS CREDITS_INPUT,
        COALESCE(TRY_PARSE_JSON(CREDITS_GRANULAR[t1.INDEX])[OBJECT_KEYS(t1.VALUE)[0]][t2.KEY][t3.KEY]:"output", 0)::FLOAT AS CREDITS_OUTPUT
    FROM MONITORING_DB.USAGE_MONITORING.STG_CORTEX_AGENT si,
        LATERAL FLATTEN(INPUT => TOKENS_GRANULAR, OUTER => TRUE) t1,
        LATERAL FLATTEN(INPUT => TRY_PARSE_JSON(t1.VALUE)[OBJECT_KEYS(t1.VALUE)[0]], OUTER => TRUE, MODE => 'OBJECT') t2,
        LATERAL FLATTEN(INPUT => t2.VALUE, OUTER => TRUE) t3
    WHERE 1 = 1
      AND t2.KEY != 'start_time'
);

CREATE OR REPLACE VIEW MONITORING_DB.AI_OBSERVABILITY.FACT_SNOWFLAKE_INTELLIGENCE_DAY_USER_AGG(
    USER_NAME,
    USAGE_DATE,
    SPEND_USD
) AS (
    SELECT
        USER_NAME,
        USAGE_DATE,
        SUM(CREDITS_INPUT + CREDITS_OUTPUT + CREDITS_READ_INPUT + CREDITS_WRITE_INPUT * 3.7) AS SPEND_USD
    FROM MONITORING_DB.AI_OBSERVABILITY.FACT_SNOWFLAKE_INTELLIGENCE_REQUESTS_DETAILS
    WHERE 1 = 1
    GROUP BY ALL
    ORDER BY 1 DESC, 2
);

