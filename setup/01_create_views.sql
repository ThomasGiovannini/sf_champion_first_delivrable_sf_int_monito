CREATE OR REPLACE VIEW MONITORING_DB.USAGE_MONITORING.STG_ROLE_TAGS(
    ROLE_NAME,
    COUNTRY,
    DIVISION,
    APPLICATION
) AS (
    WITH tagging AS (
        SELECT
            tr.OBJECT_NAME AS ROLE_NAME,
            TAG_NAME,
            TAG_VALUE
        FROM SNOWFLAKE.ACCOUNT_USAGE.TAG_REFERENCES tr
        WHERE TAG_NAME IN ('TAG_COUNTRY','TAG_DIVISION','TAG_APPLICATION')
          AND tr.DOMAIN = 'ROLE'
    )
    SELECT
        $1::TEXT AS ROLE_NAME,
        $2::TEXT AS COUNTRY,
        $3::TEXT AS DIVISION,
        $4::TEXT AS APPLICATION
    FROM tagging
    PIVOT(MAX(TAG_VALUE) FOR TAG_NAME IN ('TAG_COUNTRY', 'TAG_DIVISION', 'TAG_APPLICATION')) AS p
);

